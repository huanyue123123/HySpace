    public static void copyFiles(String source,String aim,String fileNameStart)  throws IOException{
        File dir = new File(source);
        if(dir.isDirectory()){
            File[] fileArr = dir.listFiles();
            List<File> copyList = new LinkedList<>();
            for(File file:fileArr){
                if(file.getName().split("\\.")[0].equals(fileNameStart)){
                    copyList.add(file);
                }
            }

            for(File copyFile:copyList){
                File aimdir = new File(aim + File.separator + copyFile.getName());

                BufferedOutputStream bos=new BufferedOutputStream(new FileOutputStream(aimdir));
                BufferedInputStream bis=new BufferedInputStream(new FileInputStream(copyFile));
                byte bts[]=new byte[(int)copyFile.length()];
                bis.read(bts);
                bos.write(bts);
                bos.flush();
                bis.close();
                bos.close();
            }




        }


    }
